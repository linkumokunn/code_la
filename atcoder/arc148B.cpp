	string s;cin>>s;
	int p=0,pos=0;
	for(int i=0;i<n;i++){
		if(s[i]=='p')p++;
	}
	if(p==0)cout<<s<<endl;
	else{
		while(s[pos]!='p')pos++;
		string ans=s,tmp;
		for(int i=pos;i<n;i++){
			tmp=s;
			for(int j=0;j<=i-pos;j++){
				if(s[i-j]=='p')tmp[pos+j]='d';
				if(s[i-j]=='d')tmp[pos+j]='p';
			}
			if(tmp<ans)ans=tmp;
		}
		cout<<ans<<endl;
	}
	return 0;
