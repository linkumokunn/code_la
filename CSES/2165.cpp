queue<pii> ans; 
void hanoi(int n,int a,int b,int c){
	if(n==1){
		ans.push(mp(a,b));
		return;
	}
	hanoi(n-1,a,c,b);
	ans.push(mp(a,b));
	hanoi(n-1,c,b,a);
	return ;
}
