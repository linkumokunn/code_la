void solve(int n,int order[]){
	stack<int> a,b;
	for(int i=0;i<n;i++)push_train(),b.push(i+1);
	int cnt=0;
		while(cnt<n){
		while(b.size()){
			a.push(b.top());move_station_1_to_2();
			b.pop();
			if(a.top()==order[cnt]){
				a.pop();pop_train();cnt++;
			}
		}
		while(a.size()){
			b.push(a.top());move_station_2_to_1();
			a.pop();
		}
	}
}


