	int dp[a+1],ans=0,cos=0;for(int i=0;i<=a;i++)dp[i]=-1e8;dp[0]=0;
	while(n--){
		int x,y;cin>>x>>y;	
		for(int i=a;i-y>=0;i--){
			dp[i]=max(dp[i-y]+x,dp[i]);
			if(dp[i]>ans||dp[i]==ans&&cos>i)ans=dp[i],cos=i;
		}
	}
